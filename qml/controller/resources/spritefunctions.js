function moveSpriteLeft(sprite) {
    console.log("Moving sprite left")
    sprite.x -= 10
}

function moveSpriteRight(sprite) {
    console.log("Moving sprite right")
    sprite.x += 10
}

function moveSpriteDown(sprite) {
    console.log("Moving sprite down")
    sprite.y += 10
}

function moveSpriteUp(sprite) {
    console.log("Moving sprite up")
    sprite.y -= 10
}

